import turtle
turtle.Screen().bgcolor("light pink")
v=turtle.Turtle()
#first triangle
v.forward(100)
v.left(120)
v.forward(100)

v.left(120)
v.forward(100)

v.penup()
v.right(150)
v.forward(50)

#second triangle
v.pendown()
v.right(90)
v.forward(100)

v.right(120)
v.forward(100)

v.right(120)
v.forward(100)

turtle.done()